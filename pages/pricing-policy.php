				<p><strong><u>3 Tier  system for acute treatments (one off)</u></strong><u> </u></p>
<p><br>
  <u>Rationale  for pricing tiers</u><u> </u><br>
  We  recognise that partner pharmacies serve varying demographics in terms of  socioeconomic groups as well types of customers such as local communities,  student areas or areas of high footfall such as tourist areas or transport  hubs. As a result, it is expected that partner pharmacies are likely to want  different price points that are appropriate for their customer base. At the  same time, it is problematic for our model to give partner pharmacies full  autonomy over pricing. Our balanced approach is to offer partner pharmacies the  choice of three different pricing tiers.</p>
<p><br>
  <u>2 Hour  service</u><br>
  There are  considerable profits to be made by offering a faster 1–2-hour service. We  believe there is demand for this service in all pharmacy settings but in  particular - it can be successful in high footfall areas, student areas and  tourist areas. As long as we have clinician capacity available there is no  added cost to providing this service, our system would simply put such orders  at the top of the list. We do need to look it issues related to being able to  fulfil the 1-2 hour turn around such as asking pharmacies to check if they have  a medication in stock before recommending it to patients.</p>
<p><br>
  <u>Chronic  treatment pricing</u><br>
  Chronic  treatments are treatments that are long term or permanent in nature such as ED  pills for men or birth control pills for women, hay fever treatments can be  seasonal and pain treatments for the length of time a muscle injury takes to  recover. For such treatments we must keeping pricing lower as patients are more  likely to look for cheaper options or make the effort to get a NHS prescription.  There are some treatments such as hair loss which is not available on the NHS  for which we are only considering price competition with online services, while  treatments for ED are available over the counter but we still feel patients  would want to use our service to maintain privacy. We have included 2 tiers of  pricing for such treatments – low and high. Such long-term payments are far  more lucrative for our pharmacy partners and ourselves as they are constant  income streams, e.g. if the annual income from a single patient is £40 for the  pharmacy, 100 such patients needing birth control pills would generate £4000 a  year in profits for the pharmacy, this is a reasonable target.</p>
<p><u>Monthly  service charge</u><br>
  This is not  relevant to individual treatment sale price setting but important to note. We  will charge a monthly fee to all pharmacy partners to contribute towards the IT  cost of maintaining the IT infrastructure required to keep their account  active. It will also encourage pharmacy partners to push the service otherwise  they will be paying a monthly charge without any revenue from the service. </p>
<p><u>Grouping  conditions within price range</u><u> </u><br>
  Due to  varying prices for medications, we will use average medication costs when  setting prices to be fair to our pharmacy partners who are supplying the  medication and also to keep pricing clear and constant for patients.</p>
<p>  We will  periodically review wholesale medication costs in case there are considerable  movements in price that warrant us moving a medication to a different costing  band.<br>
  Where a  medication has particular supply issues we need to be able to stop offering it  for a condition and change the default medication offer accordingly.</p>
